						<li>
						An employee recruited by promotion from the service of the Authority    shall be senior to the person recruited otherwise. provided, further, that    in the case of promotion of two or more persons, their inter se seniority    shall be determiner according to their seniority in the cadre or posts    from which they have been promoted.
						</li>
					</ol>

					<ol class="point-num-list-inner" style="list-style-type: lower-roman">
						<li>A person on deputation shall be senior to a person recruited by direct    appointment , it he opts the service of the Authority on the same date    provided that in the case of two or more persons on deputation, their    inter se seniority shall be determined, according to their seniority in     their parent service or cadre, In the case of existing staff on deputation    who have opted or likely to opt for absorption, their inter se seniority    shall be same as in their parent department/organization .</li>
					</ol>

				</li>
