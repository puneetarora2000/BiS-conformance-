		</p>
			<p>The seniority of the members of the service shall be maintained at the    level of the appointing authority.
			</p>
			<ol class="point-num-list">
				<li>
					Seniority inter se of the employee shall be determined from rank to     rank by the length of continuous service on the post in the service on    regular basis and extent of accountability. Provided that in the case of    employees appointed by direct recruitment , the order of merit     determined by the selection Committee shall not be disturbed in fixing    the seniority.
					<p>Provided further that in the case of two or more employees appointed    by different modes on the same date, their seniority shall be      determined as follows:-</p>

