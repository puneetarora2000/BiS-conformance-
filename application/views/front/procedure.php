<div class="panel panel-default">
	<div class="panel-heading"><b>Procedure for registration of area and seed processing for seed certification with PSSCA</b></div>
	<div class="panel-body text-left">
		<p>
			Any person who desire to produce certified seed in Punjab can register his area for seed certification with PSSCA. The following steps are to be taken care of, for this purpose by the concerned seed producer:
		</p>
		<ol style="list-style-type: decimal">
		<li>
			Purchase Breeder/Foundation seed from recognized institutions such as research farms of ICAR, Agricultural Universities, Govt. Seed Farms, National Seed Corporation etc.
		</li>
		<li>
			<ol style="list-style-type: lower-roman">
				<li>
					At the time of purchase of Breeder Seed, the seed producer must ensure to have cash receipt of the seed purchased and a copy of monitoring team 
					inspection report from the seller of the seed.
				</li>
				<li>
				At the time of purchase of Foundation Seed, the seed producer must ensure to have cash receipt of 'F' seed having Lot No. written on the receipt and a copy of progeny certificate of the seed lot.
				</li>
			</ol>
		<li>
			After the purchase of the Breeder/Foundation Seed, the seed producer must register his area of seed production of PSSCA in the following Performa:
			<ol style="list-style-type: lower-roman">
				<li>For the production of Foundation seed apply in PerformaA.</li>
				<li>For the production of Certified seed apply in PerformaB.</li>
			</ol>
		</li>
		<li>
			The cut off dates for registration of area with PSSCA are:
			<ol style="list-style-type: lower-alpha">
				<li>Rabi Crops  &nbsp; :	15th January</li>
				<li>Kharif Crops :	15th July</li>
			</ol>
		</li>
		<li>
			The area will be registered by PSSCA on submission os requisite application fee and inspection fee along with the prescribed Performa to the Regional Offices in the state.
		</li>
		<li>
			The seed growers may retain all the empty bags and tags of seed used for seed productions for verification of the Seed Certification official at the time of first inspection.
		</li>
		<li>
			The seed fields are inspected by PSSCA officials as per Indian Minimum Seed Certification Standards norms.
		</li>
		<li>
			The seed grower shall be required to do rouging of off type plants and other undesirable plants from the seed field as advised by the staff of PSSCA during their field inspection visits.
		</li>
			The seed from the field meeting the prescribed field standards are processed at the processing plants of the seed producer after its harvest from the field.\
		</li>
		<li>
			A representative sample is drawn by PSSCA staff from each graded seed lot for its testing from the Seed Testing Laboratory.
		</li>
		<li>
			The seed sample of seed lot meeting the prescribed seed standards is then packed in printed bags by fixing certification tags, seed producer's labels and seals of PSSCA. 
		</li>
	</ol>

	
	</div>
